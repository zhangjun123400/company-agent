## 二、核心技术挑战

### 挑战 1：语音意图识别 + 多人声纹识别并发 [严重]

在家庭环境（SNR 10-15dB）下同时做 ASR + SID，对端侧模型压缩要求极高。

**缓解措施：** 云端+端侧混合架构。ASR 走云端（<500ms），声纹注册/匹配走端侧轻量 SID 模型（~100ms），总体上保持 600ms 可接受范围。

### 挑战 2：视觉姿态估计的实时下蹲检测 [高]

端侧部署 MoveNet Lightning 在 RK3588 NPU 上每帧 ~15ms/person。RGB 相机在弱光、遮挡、快速移动场景下准确率下降。

**缓解措施：** 采用单关键点（髋部 Y 轴位移）快速判断；多人场景用声纹锁定声源方向，在该方向做单人姿态检测。

### 挑战 3：多模态时序同步 [高]

语音口令 → 声纹验证 → 视觉验证需在 ±1s 窗口内匹配。异步事件对齐是工程难点。

**缓解措施：** 事件驱动状态机 + 1.5s 滑动窗口 + 三级置信度判定（成功/提醒/二次确认）。

---

## 三、推荐技术方案：端云混合 + 状态机驱动

| 模块 | 技术选型 | 部署位置 | 延迟 |
|------|----------|----------|------|
| 语音 ASR | 云端 ASR（飞书 / iFLYTEK） | 云端 | <500ms |
| 声纹 SID | WeSpeaker-ResNet34 量化版 | 端侧 NPU | ~100ms |
| 视觉姿态 | MoveNet Lightning | 端侧 NPU | ~15ms/p |
| 游戏状态机 | 事件驱动 FSM | 端侧主控 | <10ms |
| 语音播报 | 端侧 TTS（Edgetts / MeloTTS） | 端侧 | <200ms |
| 动作控制 | 串行舵机 + 预置动作序列 | 端侧 MCU | <50ms |

### 硬件平台

