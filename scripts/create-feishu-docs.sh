## 技术约束（5 个问题）

### Q-001 [高] 语音识别引擎选型与部署方式未明确

PRD 要求"识别自然语音意图""声纹注册""多人声纹区分"，但未说明语音识别是端侧还是云端。端侧能保护隐私但精度有限；云端精度高但依赖网络、增加延迟。

**建议：** 明确端侧/云端方案，给出可接受延迟上限（如 ≤500ms）。

### Q-002 [高] 视觉识别"下蹲动作"的技术路径未定义

PRD 要求"系统通过视觉识别玩家身体明显下降"，但未说明视觉硬件（RGB 摄像头？深度相机？）及算法方案。这是本需求最核心的技术难点，影响 BOM 成本、算力和功耗。

**建议：** 明确视觉传感器规格，给出"有效下蹲"的量化标准。

### Q-003 [高] 多人声纹注册与实时识别的技术可行性未评估

PRD 要求支持 1-5 人参与，需为每名玩家做临时声纹注册并在游戏中实时区分说话者。在嘈杂家庭环境中做多人实时说话人识别，业界成熟度有限。

**建议：** 评估声纹方案在 SNR 15dB 环境下的准确率，给出降级方案。

### Q-004 [中] 机器人"下蹲"动作的硬件实现方案未提及

小小狗需要做出"四肢弯曲"动作——这直接决定机器狗的关节自由度需求。

**建议：** 明确机器狗的关节自由度需求。

### Q-005 [中] 端侧算力需求与实时性约束未评估

同时跑 ASR + 声纹识别 + 视觉姿势估计 + 游戏状态机，全流程延迟要求 ≤1s。需要评估端侧芯片算力是否够用。

**建议：** 给出端侧芯片选型目标，评估多模型并发推理的算力需求。

---

## 业务逻辑（4 个问题）

### Q-006 [中] 游戏人数 vs 机器狗资源感知未关联

PRD 要求小小狗逐个引导玩家注册，但未说明"小小狗怎么知道下一位玩家来了"。

**建议：** 明确"下一位玩家就位"的触发条件。

### Q-007 [中] 多人同时下蹲的判断逻辑不清晰

喊口令的人和下蹲的人必须是同一个人。如果旁边一位玩家也下蹲了，系统怎么区分是谁在接龙？

**建议：** 补充多人场景下动作者归属的判定逻辑。

### Q-008 [低] 身份更换机制与公平性

PRD 允许用户更换一次萝卜身份。但小小狗能否也更换？如果身份重复了怎么办？

**建议：** 定义身份池的排他逻辑。

### Q-009 [低] 游戏轮次无上限，可能导致疲劳

PRD 未定义游戏自然结束条件。

**建议：** 考虑加入"轮次提醒"，如每 15 轮小小狗建议休息。

