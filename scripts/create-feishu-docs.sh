# ── Create documents via Feishu API ──────────────────────────────────

create_doc() {
  local TITLE="$1"
  local FOLDER_TOKEN="${2:-}"

  echo "📝 Creating document: $TITLE"

  local CREATE_RESP=$(curl -s -X POST 'https://open.feishu.cn/open-apis/docx/v1/documents' \
    -H "Authorization: Bearer $TOKEN" \
    -H 'Content-Type: application/json' \
    -d "{\"title\":\"$TITLE\"}")

  local DOC_ID=$(echo "$CREATE_RESP" | jq -r '.data.document.document_id')
  local DOC_URL=$(echo "$CREATE_RESP" | jq -r '.data.document.url')

  if [ -z "$DOC_ID" ] || [ "$DOC_ID" = "null" ]; then
    echo "❌ Failed to create document: $CREATE_RESP"
    return 1
  fi

  echo "✅ Created: $DOC_URL"
  echo "$DOC_ID|$DOC_URL"
}

