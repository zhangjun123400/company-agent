## UI/UX 与交互（3 个问题）

### Q-010 [中] 规则说明与游戏启动之间缺少"测试轮"概念

儿童用户听完规则后直接进入正式游戏，可能不理解"下蹲+口令"的协同。

**建议：** 增加可选的"慢速演示轮"。

